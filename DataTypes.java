package javapractice;

public class DataTypes {

	public static void main(String[] args) {
	      byte a =101;
	      System.out.println("this is a byte datatype" +" "+ a );
	      
	      short b = 12341;
	      System.out.println("this is a short datatype" +" "+b);
	      
	      int c =1234121221;
	      System.out.println("this is a int datatype" +" "+c);
	      
	      long d =12122112221l;
	      System.out.println("this is a long datatype" +" "+d);
	      
	      double e=12.12121212121;
	      System.out.println("this is a double datatype" +" "+e);
	      
	      float f =12.1212f;
	      System.out.println("this is a float datatype" +" "+f);
	      
	      boolean flag= true;
	      System.out.println("this is a boolean datatype" +" "+flag);
	      
	      char w='a';
	      System.out.println("this is a char datatype" +" "+w);
	      

	}

}
